package io.github.yoimerdr.compose.multiplatform.virtualjoystick.core.control

/**
 * Enum class representing the different types of backgrounds
 * that can be used in the joystick.
 */
enum class BackgroundType {
    /** Default background type. */
    Default,

    /** Modern style background. */
    Modern,

    /** Classic style background. */
    Classic,

    /** Modern D-pad style background. */
    DpadModern,

    /** Classic D-pad style background. */
    DpadClassic,

    /** Standard D-pad style background. */
    DpadStandard;
}